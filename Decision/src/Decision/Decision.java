package Decision;

public class Decision
{
	public static void main(String[] args)
	{
		int i = 1;
		int j = 1;
		if (i == 1 && j == 1)
			System.out.println("i is 1 and j is 1");
		if (i == 1 || j == 2)
			System.out.println("Either i is 1 or j is 1");
		if ((i != 1))
			System.out.println("i is not 1");
	}
}


